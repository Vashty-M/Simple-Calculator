# Python code to demonstrate the working of
# len(), min() and max()
# initializing list 1
a_list = [2, 1, 3, 5, 4]

maximum=max(a_list)

minimum= min(a_list)
length=len(a_list)

# using len() to print length of list
print("The length of list is : ", length)

# using min() to print minimum element of list
print("The minimum element of list is : ", minimum)


# using max() to print maximum element of list
print("The maximum element of list is :", maximum)
